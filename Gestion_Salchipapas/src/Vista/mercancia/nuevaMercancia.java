    private void btn_buscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_buscarActionPerformed
        // TODO add your handling code here:
        if (!txt_id.getText().isEmpty()) {
            int mrId = Integer.parseInt(txt_id.getText());
            Mercancia mrFind = objMercancia.obtenerMercanciaId(mrId);
            if (mrFind != null) { //Encontro algo
                txt_id.setEnabled(false); //Inhabilitamos el campo id para que no lo modifique
                btn_guardar.setEnabled(false);//Inhabilitamos el btn de guardar para que no guarde datos repetidos
                btn_modificar.setEnabled(true);
                btn_eliminar.setEnabled(true);
                txt_descripcion.setText(mrFind.getDescripcion());
                txt_cantidad.setText("" + mrFind.getCantidad());
                for (int i = 0; i < cbx_TipoMercancia.getItemCount(); i++) {
                    if (cbx_TipoMercancia.getItemAt(i).equals(mrFind.getCategoria())) {
                        cbx_TipoMercancia.setSelectedItem(mrFind.getCategoria());
                    }
                }
            }
        }

    }//GEN-LAST:event_btn_buscarActionPerformed

    private void btn_limpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_limpiarActionPerformed
        // TODO add your handling code here:
        limpiarCampos(); //Limpiamos los campos y habilitamos botones
    }//GEN-LAST:event_btn_limpiarActionPerformed

    private void btn_guardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_guardarActionPerformed
        // TODO add your handling code here:
        if (!(txt_descripcion.getText().isEmpty() && txt_cantidad.getText().isEmpty())) {
            String categoria = cbx_TipoMercancia.getSelectedItem().toString();
            String descripcion = txt_descripcion.getText();
            int cantidad = Integer.parseInt(txt_cantidad.getText());
            Mercancia newMercancia = new Mercancia(0, categoria, descripcion, cantidad);
            if (objMercancia.AgregarMercancia(newMercancia)) {
                limpiarCampos();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Faltan Datos");
            txt_descripcion.requestFocus();
        }
    }//GEN-LAST:event_btn_guardarActionPerformed

    private void btn_modificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_modificarActionPerformed
        // TODO add your handling code here:
        int mr_id = Integer.parseInt(txt_id.getText());
        String categoria = cbx_TipoMercancia.getSelectedItem().toString();
        String descripcion = txt_descripcion.getText();
        int cantidad = Integer.parseInt(txt_cantidad.getText());
        Mercancia mr = new Mercancia(mr_id, categoria, descripcion, cantidad);
        boolean isUpdate = objMercancia.actualizarMercancia(mr);
        if(isUpdate){
            limpiarCampos();
        }
    }//GEN-LAST:event_btn_modificarActionPerformed

    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        // TODO add your handling code here:
        int mr_id = Integer.parseInt(txt_id.getText());
        int opcion = JOptionPane.showConfirmDialog(this, "Â¿Desea eliminar la mercancia?",
                "Confirmacion", JOptionPane.YES_NO_OPTION);
        if (opcion == JOptionPane.YES_OPTION) {
            objMercancia.eliminarMercancia(mr_id);
            limpiarCampos();
        }
    }//GEN-LAST:event_btn_eliminarActionPerformed

